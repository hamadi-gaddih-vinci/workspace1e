public class Rectangle {
    double longueur;
    double largeur;

    Rectangle(double nLongueur, double nLargeur) {
        longueur = nLongueur;
        largeur = nLargeur;
    }
    double calculerAire() {
        return largeur * longueur;
    }
    double caclculerPerimetre(){
        return (largeur + longueur) *2;
    }






}


























